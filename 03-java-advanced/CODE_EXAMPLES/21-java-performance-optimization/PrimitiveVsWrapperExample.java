public class PrimitiveVsWrapperExample {

    public static void main(String[] args) {

        int primitiveValue = 100;

        Integer wrapperValue = 100;

        System.out.println(
                "Primitive: "
                        + primitiveValue
        );

        System.out.println(
                "Wrapper: "
                        + wrapperValue
        );
    }
}